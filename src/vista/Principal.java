    private void eliminardeptxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminardeptxtActionPerformed
        String selectedDepartment = listadepartamento.getSelectedValue();
        if (selectedDepartment != null) {
            departamentosnuevos.eliminarDepartamento(selectedDepartment);
            departamentosnuevos.obtenerDepartamentos(listadepartamento);
            textgerente.setText("");
            textnumero.setText("");
            JOptionPane.showMessageDialog(this, "Departamento eliminado");
        } else {
            System.out.println("Departamento no seleccionado.");
        }
    }//GEN-LAST:event_eliminardeptxtActionPerformed

